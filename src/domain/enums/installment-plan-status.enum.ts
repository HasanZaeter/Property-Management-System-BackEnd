
export enum InstallmentPlanStatus {
  /** تم إنشاء الخطة لكنها لم تبدأ بعد (قبل أول قسط) */
  PENDING = 'قيد الانتظار',

  /** الخطة نشطة حالياً ويتم دفع الأقساط شهرياً */
  ACTIVE = 'نشطة',

  /** الخطة مكتملة وتم دفع جميع الأقساط */
  COMPLETED = 'مكتملة',

  /** الخطة متخلفة عن الدفع (لم يتم دفع قسط في موعده) */
  DEFAULTED = 'متأخرة عن الدفع',

  /** الخطة ألغيت قبل الاكتمال (مثلاً ألغى المستخدم الحجز) */
  CANCELLED = 'ملغاة',
}